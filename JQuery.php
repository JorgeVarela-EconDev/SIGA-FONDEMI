  <h3>Seccion 1: ¿Qué es un sistema operativo y cuál es su función principal?</h3>
  <div>
    <p>
    Un sistema operativo es un programa informático que funciona como un enlace entre los usuarios de un ordenador y su hardware. 
    Su tarea principal consiste en administrar los recursos del ordenador, como la memoria, el procesador, los dispositivos de almacenamiento y los periféricos, 
    ofreciendo un ambiente estable y uniforme para que las aplicaciones puedan funcionar.
    </p>
  </div>
  <h3>Seccion 2: Cuáles son los tipos más comunes de sistemas operativos?</h3>
  <div>
    <p>
    Windows: Desarrollado por Microsoft, es uno de los sistemas operativos más utilizados en computadoras personales.</p>
    macOS: Desarrollado por Apple, es el sistema operativo utilizado en las computadoras Macintosh.</p>
    Linux: Un sistema operativo de código abierto, muy popular en servidores y entusiastas de la tecnología.</p>
    Android: Un sistema operativo basado en Linux, diseñado principalmente para dispositivos móviles y desarrollado por Google.</p>
    iOS: Desarrollado por Apple, es el sistema operativo utilizado en los dispositivos móviles de la marca, como iPhone y iPad.</p>
    </p>
  </div>
  <h3>Seccion 3: ¿Cuáles son las diferencias entre un sistema operativo de escritorio y un sistema operativo móvil? </h3>
  <div>
    <p>
    Plataforma de usuario: Los sistemas operativos para ordenadores están concebidos para emplearse con teclados y ratones, en cambio, los sistemas operativos para dispositivos móviles están diseñados para pantallas táctiles.</p>
    Administrar recursos: Los sistemas operativos para dispositivos móviles están concebidos para maximizar la eficiencia en la utilización de recursos, a causa de las restricciones de hardware de los aparatos móviles.</p>
    Implementaciones: Las aplicaciones más complejas y potentes suelen ser ejecutadas por sistemas operativos de escritorio, en cambio, los sistemas operativos para móviles están diseñados para aplicaciones más livianas y específicas para dispositivos móviles.</p>
    Protección: Los sistemas operativos para dispositivos móviles generalmente implementan medidas de seguridad más rigurosas debido al carácter portátil de los aparatos y la exigencia de salvaguardar información personal y económica.</p>
    </p>
  </div>
  <h3>Seccion 4: ¿Qué es el kernel de un sistema operativo y cuál es su importancia? </h3>
  <div>
    <p>
    El núcleo del sistema operativo se conoce como el kernel, es el elemento clave que administra los recursos del hardware y brinda servicios fundamentales a las otras partes del sistema operativo y a las aplicaciones. 
    El núcleo se encarga de funciones vitales como la administración de la memoria, la organización de procesos, la gestión de interrupciones y la interacción entre aparatos. Su relevancia se basa en que sin el núcleo, el sistema operativo no podría operar adecuadamente, 
    dado que no existiría un control eficaz y seguro de los recursos del hardware.
    </p>
  </div>
