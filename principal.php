        <section id="mision">
            <h1>Misión</h1>
            <p>
                La misión de <strong>FONDEMI</strong> es la de promover y desarrollar un sistema de economía comunal que garantice la autogestión de las comunidades y consolide el Poder Popular organizado. Fondemi se dedica a impulsar proyectos socioproductivos que fomenten el desarrollo económico y social de las comunidades, proporcionando financiamiento, capacitación y asesoría técnica. La misión de Fondemi abarca la creación de condiciones favorables para que las comunidades puedan diseñar, ejecutar y gestionar proyectos productivos que mejoren su calidad de vida y contribuyan al desarrollo sostenible del país. <strong>FONDEMI</strong> trabaja en estrecha colaboración con las comunidades para identificar sus necesidades y potencialidades, apoyándolas en la formación de redes de cooperación y solidaridad que fortalezcan el tejido social y productivo. Además, Fondemi se compromete a promover la inclusión social, la equidad de género y la participación activa de todas las personas en los procesos de desarrollo comunal, asegurando que los beneficios del desarrollo lleguen a todos los sectores de la sociedad.
            </p>
        </section>

        <section id="Nuestra comunidad">
            <h1>Nuestra Comunidad</h1>
            <p>
                La comunidad de <strong>FONDEMI</strong> está integrada por una red de comunidades organizadas y Bancos Comunales que colaboran para promover la economía comunal y el desarrollo sostenible. Los Bancos Comunales son entidades flexibles, democráticas y solidarias que gestionan recursos financieros y no financieros de los Consejos Comunales.

                Además, <strong>FONDEMI</strong> trabaja con diversas organizaciones, tanto gubernamentales como no gubernamentales, como asociaciones civiles, cooperativas y fundaciones, para diseñar y ejecutar proyectos que mejoren la calidad de vida y contribuyan al desarrollo integral del país.
            </p>
